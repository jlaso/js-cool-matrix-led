/** The constructor for CoolLedMatrix **/
function CoolLedMatrix(container, id) {

    this.id = id;
    this.animateInterval = null;

    var width = 121;
    var html = '<div class="led" id="led_' + id + '" style="' +
        'position: absolute;' +
        'top: 0;' +
        'left: ' + id * (width + 10) + 'px;' +
        'width: ' + width + 'px;' +
        'height: ' + width + 'px;' +
        'border-radius: 4px; ' +
        'padding-left: 7px;' +
        'padding-bottom: 7px;' +
        'box-shadow: 0 1px 0 #fff, inset 0 1px #555;' +
        'border: 1px solid #333;' +
        'background: rgb(56, 56, 56);' + /* Old browsers */
        'background: -moz-linear-gradient(top, rgba(56, 56, 56, 1) 0%, rgba(33, 33, 33, 1) 100%);' + /* FF3.6+ */
        'background: -webkit-gradient(linear, left top, left bottom, color-stop(0%, rgba(56, 56, 56, 1)), color-stop(100%, rgba(33, 33, 33, 1)));' + /* Chrome,Safari4+ */
        'background: -webkit-linear-gradient(top, rgba(56, 56, 56, 1) 0%, rgba(33, 33, 33, 1) 100%);' + /* Chrome10+,Safari5.1+ */
        'background: -o-linear-gradient(top, rgba(56, 56, 56, 1) 0%, rgba(33, 33, 33, 1) 100%);' + /* Opera 11.10+ */
        'background: -ms-linear-gradient(top, rgba(56, 56, 56, 1) 0%, rgba(33, 33, 33, 1) 100%);' + /* IE10+ */
        'background: linear-gradient(to bottom, rgba(56, 56, 56, 1) 0%, rgba(33, 33, 33, 1) 100%);' + /* W3C */
        'filter: progid:DXImageTransform.Microsoft.gradient(startColorstr="#383838", endColorstr="#212121", GradientType=0);' + /* IE6-9 */
        ">" +
        '<ul style="list-style: none; margin: 0; padding: 0;">';
    for (var i = 0; i <= 120; i++) {
        html += "<li></li>";
    }
    html += "</ul></div>";
    $(container).append(html);

    this.reset = function () {
        $("#led_" + this.id + " ul li").each(function (index) {
            $(this).removeClass();
        });
    };

    this.draw = function (symbol, colorClass) {
        this.reset();
        $("#led_" + this.id + " ul li").each(function (index) {
            if (symbol[index] != 0) {
                $(this).addClass(colorClass);
            }
        });
    };

    this.drawPalette = function (symbol, palette) {
        this.reset();
        $("#led_" + this.id + " ul li").each(function (index) {
            $(this).addClass(palette.get(symbol[index]));
        });
    };

    this.animatePalette = function (symbol, palette, timeout, times, callback) {
        this.palette = palette;
        var that = this;
        this.animateInterval = setInterval(function () {
            that.drawPalette(symbol, that.palette);
            that.palette = callback(that.palette);
            if (times !== null) {
                times--;
                if (times === 0) {
                    that.stopAnimate();
                }
            }
        }, timeout);
    };

    this.animate = function (symbolsMatrix, colorClass, timeout, times) {
        var that = this;
        that.animateCurrent = 0;
        this.animateInterval = setInterval(function () {
            that.draw(symbolsMatrix[that.animateCurrent], colorClass);
            if (times !== null) {
                times--;
                if (times === 0) {
                    that.stopAnimate();
                }
            }
            that.animateCurrent++;
            if (that.animateCurrent >= symbolsMatrix.length) {
                that.animateCurrent = 0;
            }
        }, timeout);
    };

    this.stopAnimate = function () {
        if (this.animateInterval != null) {
            clearInterval(this.animateInterval);
            this.animateInterval = null;
        }
    }
}

function Palette(colors) {
    this.colors = [];
    this.uuid = "p" + Date.now();
    var defs = "";
    for (var i = 0; i < colors.length; i++) {
        var code = this.uuid + "_" + i;
        this.colors.push(code);
        defs += ".led ul li." + code + " { background: " + colors[i] + ";box-shadow: 0 0 16px " + colors[i] + ";}";
    }
    $("<style>").prop("type", "text/css").html(defs).appendTo("head");

    this.get = function (index) {
        return this.colors[index];
    }
}

